print('Введите координаты первой точки через запятую [x1, y1, z1]: ')
x1, y1, z1 = list(map(float, (input().split(','))))
print('Введите координаты второй точки [x2, y2, z2]: ')
x2, y2, z2 = list(map(float, (input().split(','))))

print('Расстояние между двумя точками пространства: ')
print(round(((x2 - x1) ** 2 + (y2 - y1) ** 2 + (z2 - z1) ** 2) ** (0.5),3))